//reference types
//object
let person = {
    username:'yuti',
    middlename:'naresh',
    lastname:'meher',
    age:23
};
console.log(person);

//dot notation
person.username = 'jaee';
console.log(person.username);
console.log(person);

//bracket notation
let seletion = 'username';
person[seletion] = 'deep';
console.log(person.username);


